/**
 * Describes the available sizing options of most components. NOTE: Some components use a custom enum which expands on
 * the NeonSize options.
 */
export enum NeonSize {
  /** A small component size, typically has a default of 32 rem. */
  Small = 's',
  /** A medium component size, usually the default size. Which typically has a default of 40 rem. */
  Medium = 'm',
  /** A large component size, typically has a default of 48 rem. */
  Large = 'l',
}
