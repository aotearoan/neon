/**
 * Describes the different sizes of <a href="/user-input/button">NeonButton</a> components.
 */
export enum NeonButtonSize {
  /** Small buttons are by default <em>32 rem</em> in size */
  Small = 's',
  /** Medium buttons are by default <em>40 rem</em> in size */
  Medium = 'm',
  /** Large buttons are by default <em>48 rem</em> in size */
  Large = 'l',
  /** Extra large buttons are by default <em>64 rem</em> in size */
  ExtraLarge = 'xl',
}
