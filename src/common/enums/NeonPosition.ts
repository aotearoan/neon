/**
 * Placement directions used for placement of the <a href="/feedback/tooltip">NeonTooltip</a> &
 * <a href="/layout/drawer">NeonDrawer</a> components when active.
 */
export enum NeonPosition {
  /** Place the tooltip contents <em>above</em> the trigger element. */
  Top = 'top',
  /** Place the tooltip contents <em>below</em> the trigger element. */
  Left = 'left',
  /** Place the tooltip contents to the <em>left</em> of the trigger element. */
  Bottom = 'bottom',
  /** Place the tooltip contents to the <em>right</em> of the trigger element. */
  Right = 'right',
}
